    private void showPelanggan() {
        DefaultTableModel data = new DefaultTableModel(null, Config.TABLE_HEADER_PELANGGAN);
        tblPelanggan.setModel(data);
        ArrayList<PelangganModel> listPelanggan = mPelangganPresenter.getData();
        for (PelangganModel item : listPelanggan) {
            String[] kolom = {item.idPelanggan, item.nama, item.alamat, item.telepon};
            data.addRow(kolom);
        }
    }

    private void showTiket() {
        DefaultTableModel data = new DefaultTableModel(null, Config.TABLE_HEADER_TIKET);
        tblTiket.setModel(data);
        ArrayList<TiketModel> list = mTiketPresenter.getData();
        for (TiketModel item : list) {
            String[] kolom = {item.kodeTicket, item.maskapai, item.kelas, item.tujuan, String.valueOf(item.harga), item.status};
            data.addRow(kolom);
        }
    }

