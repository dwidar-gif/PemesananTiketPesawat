    private void save() {
        try {
            ArrayList<PenjualanModel> list = new ArrayList<>();
            Date skrg = new Date();
            SimpleDateFormat frm = new SimpleDateFormat("yyyy-MM-dd", new Locale("id"));
            String tgl = frm.format(skrg);

            int t = tblPenjualan.getRowCount();
            for (int i = 0; i < t; i++) { // merge into new object
                String idPelanggan = tblPenjualan.getValueAt(i, 1).toString();
                String idTiket = tblPenjualan.getValueAt(i, 3).toString();
                double harga = Double.parseDouble(tblPenjualan.getValueAt(i, 7).toString());
                int jumlah = Integer.parseInt(tblPenjualan.getValueAt(i, 8).toString());
                double total = Double.parseDouble(tblPenjualan.getValueAt(i, 9).toString());

                PelangganModel pelanggan = mPresenter.findPelangganById(idPelanggan);
                TiketModel tiket = mPresenter.findTiketById(idTiket);

                PenjualanModel model = new PenjualanModel();
                model.idPenjualan = txtKodeTransaksi.getText();
                model.tglBerangkat = frm.format(txtTanggalBerangkat.getDate());
                model.idPelanggan = pelanggan.idPelanggan;
                model.namaPelanggan = pelanggan.nama;
                model.alamatPelanggan = pelanggan.alamat;
                model.teleponPelanggan = pelanggan.telepon;

                model.kodeTicket = tiket.kodeTicket;
                model.maskapai = tiket.maskapai;
                model.kelas = tiket.kelas;
                model.tujuan = tiket.tujuan;
                model.harga = tiket.harga;
                model.jumlahBayar = Double.parseDouble(txtTotal.getText());
                model.tiketStatus = tiket.status;

                model.jumlahTiket = jumlah;
                model.totalHarga = total;
                model.createTime = tgl;
                model.status = "berhasil";

                list.add(model);
            }

            mPresenter.save(list);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

